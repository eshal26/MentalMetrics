def generate_report(analysis_result: dict) -> dict:
    payload = {
        "contents": [{"parts": [{"text": _build_prompt(analysis_result)}]}],
        "generationConfig": {
            "temperature": 0.15,
            "maxOutputTokens": 3072,
            "topP": 0.8,
        },
    }

    last_error: str | None = None
    for attempt in range(3):
        try:
            resp = requests.post(
                GEMINI_MODEL_URL,
                headers={
                    "x-goog-api-key": GEMINI_API_KEY,
                    "Content-Type": "application/json",
                },
                json=payload,
                timeout=60,
            )
        except requests.exceptions.RequestException as exc:
            last_error = f"Network error: {exc}"
            if attempt < 2:
                time.sleep(1.5 * (attempt + 1))
            continue

        if resp.status_code == 200:
            raw_text = resp.json()["candidates"][0]["content"]["parts"][0]["text"]
            parsed = _parse_sections(raw_text)
            for key in parsed:
                parsed[key] = _clean_report_text(parsed[key])
            parsed["raw_report_text"] = _clean_report_text(raw_text)
            parsed["source"] = "gemini"
            return parsed

        last_error = f"Gemini API error {resp.status_code}: {resp.text}"
        if resp.status_code not in {429, 500, 503} or attempt == 2:
            break
        time.sleep(1.5 * (attempt + 1))

    return _build_local_report(analysis_result, technical_note=last_error)
