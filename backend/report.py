def _parse_sections(raw_text: str) -> dict:
    headings = [(title, key) for key, title in REPORT_SECTIONS]
    sections = {key: "" for key, _ in REPORT_SECTIONS}
    current = None

    for line in raw_text.split("\n"):
        stripped = line.strip()
        normalized = stripped.lstrip("#").strip()
        matched = None
        for heading, key in headings:
            pattern = rf"^(?:\d+[\).\s-]+)?{re.escape(heading)}\s*:?\s*(.*)$"
            match = re.match(pattern, normalized, flags=re.IGNORECASE)
            if match:
                matched = key
                trailing = match.group(1).strip()
                if trailing:
                    sections[key] += trailing + "\n"
                break
        if matched:
            current = matched
        elif current:
            sections[current] += line + "\n"

    for key in sections:
        sections[key] = re.sub(r"^Subject ID\s*\n", "Subject ID: ", sections[key])
        sections[key] = sections[key].strip()
    return sections


def _clean_report_text(text: str) -> str:
    """Remove any leaked metric names the model failed to suppress."""
    cleaned = re.sub(
        r"\s*\([^)]*(?:TCAV|mean_dd|directional derivative)[^)]*\)", "",
        text, flags=re.IGNORECASE)
    cleaned = re.sub(r"[^.!\n]*\bTCAV\b[^.!\n]*[.!\n]?", "", cleaned, flags=re.IGNORECASE)
    cleaned = re.sub(r"[^.!\n]*\bmean_dd\b[^.!\n]*[.!\n]?", "", cleaned, flags=re.IGNORECASE)
    cleaned = re.sub(
        r"[^.!\n]*\bdirectional derivative\b[^.!\n]*[.!\n]?", "", cleaned, flags=re.IGNORECASE)
    cleaned = re.sub(r"\n{3,}", "\n\n", cleaned)
    return cleaned.strip()


def _confidence_band(confidence_pct: float) -> str:
    if confidence_pct > 90:
        return "High confidence"
    if confidence_pct >= 70:
        return "Moderate confidence"
    return "Low confidence"


def _temporal_consistency(probabilities: list[float]) -> str:
    if not probabilities:
        return "marked variability across recording segments"
    prob_std = pstdev(probabilities) if len(probabilities) > 1 else 0.0
    if prob_std < 0.05:
        return "highly stable across the recording"
    if prob_std < 0.12:
        return "generally stable with minor fluctuations"
    return "marked variability across recording segments"


def _segment_summary(
    probabilities: list[float],
) -> tuple[float | None, float, float | None, float | None]:
    if not probabilities:
        return None, 0.0, None, None
    mean_val = sum(probabilities) / len(probabilities)
    std_val = pstdev(probabilities) if len(probabilities) > 1 else 0.0
    return mean_val, std_val, min(probabilities), max(probabilities)


def _concept_family(concept_name: str) -> str:
    if concept_name in {"FAA", "Theta", "Alpha_Power", "Beta_Power"}:
        return "spectral power"
    if concept_name == "TBR":
        return "spectral ratios"
    if concept_name == "Coherence":
        return "connectivity"
    return "mixed features"


def _concept_prompt_payload(analysis_result: dict) -> dict:
    concepts = []
    for concept in analysis_result.get("concept_summaries", []):
        tcav = float(concept.get("tcav_score", 0.0))
        mean_dd = float(concept.get("mean_derivative", 0.0))
        flag = _normalise_flag(concept.get("clinical_flag", "WEAK"))
        concepts.append(
            {
                "name": concept.get("concept_name"),
                "tcav_score": tcav,
                "mean_dd": mean_dd,
                "std_dd": float(concept.get("tcav_std", 0.0)),
                "cav_accuracy": float(concept.get("cav_accuracy", 0.0)),
                "clinical_flag": flag,
                "description": concept.get("description", ""),
                "clinical_note": concept.get("clinical_note", ""),
                "mdd_direction": concept.get("mdd_direction", ""),
            }
        )
    return {"concepts": concepts}


